package com.example.ludioil.ludihexiao.bean;

public class OilRecordBean {
    public String time;
    public String money;
    public String Fluid;
    public String PayMount;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getFluid() {
        return Fluid;
    }

    public void setFluid(String fluid) {
        Fluid = fluid;
    }

    public String getPayMount() {
        return PayMount;
    }

    public void setPayMount(String payMount) {
        PayMount = payMount;
    }
}
